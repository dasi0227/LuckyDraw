package com.dasi.domain.strategy.service.armory;

import com.dasi.domain.strategy.model.entity.StrategyAwardEntity;
import com.dasi.domain.strategy.model.entity.StrategyEntity;
import com.dasi.domain.strategy.model.entity.StrategyRuleEntity;
import com.dasi.domain.strategy.repository.IStrategyRepository;
import com.dasi.types.common.Constants;
import com.dasi.types.enums.ResponseCode;
import com.dasi.types.exception.AppException;
import lombok.extern.slf4j.Slf4j;
import org.springframework.stereotype.Service;

import javax.annotation.Resource;
import java.math.BigDecimal;
import java.math.RoundingMode;
import java.security.SecureRandom;
import java.util.Map.Entry;
import java.util.*;

@Slf4j
@Service
public class StrategyArmoryDispatch implements IStrategyArmory, IStrategyDispatch {

    @Resource
    private IStrategyRepository repository;

    @Override
    public boolean assembleLotteryStrategy(Long strategyId) {
        // ====================
        // 查询策略配置
        // ====================
        // 1. 查询当前策略的奖品列表
        List<StrategyAwardEntity> strategyAwardEntities = repository.queryStrategyAwardList(strategyId);
        // 2. 直接将策略id作为key，然后装配奖品，此时没有任何规则应用，只有单纯的概率模型
        assembleLotteryStrategy(String.valueOf(strategyId), strategyAwardEntities);


        // ====================
        // 权重策略配置
        // ====================
        // 1. 查询当前策略
        StrategyEntity strategyEntity = repository.queryStrategyEntityByStrategyId(strategyId);
        if (!strategyEntity.hasRuleWeight()) return true;
        // 2. 查询策略中的权重策略规则
        StrategyRuleEntity strategyRuleEntity = repository.queryStrategyRuleByRuleModel(strategyId, Constants.RuleModel.RULE_WEIGHT);
        if (null == strategyRuleEntity) {
            throw new AppException(ResponseCode.STRATEGY_RULE_WEIGHT_IS_NULL.getCode(), ResponseCode.STRATEGY_RULE_WEIGHT_IS_NULL.getInfo());
        }
        // 3. 查询权重策略规则中的积分值和奖品列表
        Map<String, List<Integer>> ruleWeight = strategyRuleEntity.getRuleWeightValue();
        for (Entry<String, List<Integer>> entry : ruleWeight.entrySet()) {
            // 4. 去除不在奖品列表的奖品
            ArrayList<StrategyAwardEntity> strategyAwardEntitiesClone = new ArrayList<>(strategyAwardEntities);
            strategyAwardEntitiesClone.removeIf(entity -> !entry.getValue().contains(entity.getAwardId()));
            assembleLotteryStrategy(String.valueOf(strategyId).concat(Constants.UNDERSCORE).concat(entry.getKey()), strategyAwardEntitiesClone);
        }

        return true;
    }

    private void assembleLotteryStrategy(String key, List<StrategyAwardEntity> entities) {
        // 1. 获取最小概率
        BigDecimal minValue = entities.stream()
                .map(StrategyAwardEntity::getAwardRate)
                .min(BigDecimal::compareTo)
                .orElse(BigDecimal.ZERO);

        // 2. 获取概率总和
        BigDecimal sumValue = entities.stream()
                .map(StrategyAwardEntity::getAwardRate)
                .reduce(BigDecimal.ZERO, BigDecimal::add);

        // 3. 获取概率长度：把最小概率当作基本单位，计算概率总和对应多少个基本单位，并向上取整数
        BigDecimal rateRange = sumValue.divide(minValue, 0, RoundingMode.CEILING);

        // 4. 计算每个倍率与最小概率的比值，也即对应多少个基本单位，将对应数量的 awardId 加入概率奖品数组
        ArrayList<Integer> strategyAwardArray = new ArrayList<>(rateRange.intValue());
        for (StrategyAwardEntity entity : entities) {
            Integer awardId = entity.getAwardId();
            BigDecimal awardRate = entity.getAwardRate();
            int amount = rateRange.multiply(awardRate).setScale(0, RoundingMode.CEILING).intValue();
            strategyAwardArray.addAll(Collections.nCopies(amount, awardId));
        }

        // 5. 打乱概率奖品数组
        Collections.shuffle(strategyAwardArray);

        // 6. 将 Array 变成 Map，并将索引作为 key，从而利用 Redis 的 HSET 提升查找性能
        Map<String, String> strategyAwardMap = new HashMap<>();
        for (int i = 0; i < strategyAwardArray.size(); i++) {
            strategyAwardMap.put(String.valueOf(i), String.valueOf(strategyAwardArray.get(i)));
        }

        // 7. 将 Map 存储到 Redis
        repository.storeStrategyAwardRate(key, strategyAwardMap.size(), strategyAwardMap);
    }

    @Override
    public Integer getRandomAwardId(Long strategyId) {
        String key = String.valueOf(strategyId);
        return getRandomAwardId(key);
    }

    @Override
    public Integer getRandomAwardId(Long strategyId, String ruleWeight) {
        String key = String.valueOf(strategyId).concat(Constants.UNDERSCORE).concat(ruleWeight);
        return getRandomAwardId(key);
    }

    private Integer getRandomAwardId(String key) {
        // 1. 获取概率长度
        int rateRange = repository.getRateRange(key);
        // 2. 生成随机数，找到对应的概率奖品
        return repository.getStrategyAwardAssemble(key, new SecureRandom().nextInt(rateRange));
    }

}
